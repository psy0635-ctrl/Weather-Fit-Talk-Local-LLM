"""Ollama LLM에 보낼 프롬프트를 만드는 파일.

──────────────────────────────────────────────────────────────────────────────
[프롬프트(Prompt)란?]

LLM(대형 언어 모델)은 입력으로 받은 텍스트(= 프롬프트)를 보고
그 다음에 올 텍스트를 예측해서 답변을 생성합니다.

프롬프트는 AI에게 보내는 "지시서"입니다.
  - "너는 어떤 역할인지" (SYSTEM_LAYER)
  - "무엇을 하면 안 되는지" (SAFETY_LAYER)
  - "어떤 기준으로 판단해야 하는지" (RULE_LAYER, RECOMMENDATION_LAYER)
  - "현재 상황이 어떤지" (CONTEXT_LAYER)
  - "어떤 형식으로 답해야 하는지" (OUTPUT_FORMAT_LAYER)

이 파일은 여러 개의 LAYER 문자열을 만든 뒤,
build_weather_fit_prompt 함수에서 하나로 합쳐 최종 프롬프트를 반환합니다.
──────────────────────────────────────────────────────────────────────────────
"""


# ──────────────────────────────────────────────────────────────────────────────
# SYSTEM_LAYER: AI의 기본 역할을 정합니다.
# "너는 ~이야" 형태로 시작해서 AI가 어떤 페르소나로 답해야 하는지 알려줍니다.
# 역할을 명확히 줄수록 답변의 방향이 안정적으로 유지됩니다.
# ──────────────────────────────────────────────────────────────────────────────
SYSTEM_LAYER = """
너는 날씨와 상황에 맞는 옷차림을 추천하는 AI 스타일리스트야.
이름은 WEATHER STYLIST이며, 사용자의 질문에서 추론한 최종 날씨와 최종 스타일을 가장 우선해서 코디를 제안해.
"""

# ──────────────────────────────────────────────────────────────────────────────
# SAFETY_LAYER: AI가 출력하면 안 되는 것들을 명확히 금지합니다.
# Streamlit 화면에는 일반 텍스트 답변만 보여주는 것이 목적입니다.
# LLM이 가끔 HTML/CSS 코드 조각을 답변에 섞는 경우가 있기 때문에 명시적으로 막습니다.
# ──────────────────────────────────────────────────────────────────────────────
SAFETY_LAYER = """
HTML 코드, CSS 코드, div 태그, span 태그, class 속성, style 속성, markdown 코드블록은 절대 출력하지 마.
UI, 버튼, 화면, 레이아웃, 개발 관련 단어도 출력하지 마.
답변은 반드시 순수한 한국어 텍스트로만 작성해.
"""

# ──────────────────────────────────────────────────────────────────────────────
# USER_INPUT_PRIORITY_LAYER: 사용자가 직접 쓴 조건을 최우선으로 보라는 규칙입니다.
# 예: 사이드바는 "맑음"이어도 사용자가 "비 와"라고 쓰면 비를 우선 처리해야 합니다.
# LLM은 이 규칙을 명시적으로 받아야 사이드바보다 실시간 입력을 우선합니다.
# ──────────────────────────────────────────────────────────────────────────────
USER_INPUT_PRIORITY_LAYER = """
사용자 입력 우선 규칙:
- 사용자 입력에서 추론한 날씨, 기온, 바람, 스타일이 사이드바 값과 다르면 사용자 입력을 우선한다.
- 프롬프트에 들어온 최종 날씨, 최종 기온, 최종 바람, 최종 스타일만 기준으로 답한다.
- 사용자가 "덥다", "더워", "습하다", "땀난다"라고 입력했다면 더운 날씨 기준으로 추천한다.
- 사용자가 "춥다", "추워", "쌀쌀하다"라고 입력했다면 추운 날씨 기준으로 추천한다.
- 답변에서 "영하", "패딩", "두꺼운 코트" 같은 추위 표현은 최종 기온이 영하 또는 0~5도일 때만 사용한다.
- 최종 기온이 26~30도 또는 31도 이상이면 반팔, 얇은 소재, 통기성, 땀 대응을 우선하고 겨울 아이템을 추천하지 않는다.
"""

# ──────────────────────────────────────────────────────────────────────────────
# STYLE_PRIORITY_LAYER: 스타일 선택을 어떻게 해석할지 알려줍니다.
# "자동 추천"인지, 특정 스타일 고정인지에 따라 답변 방식이 달라집니다.
# ──────────────────────────────────────────────────────────────────────────────
STYLE_PRIORITY_LAYER = """
스타일 우선 규칙:
- 추천 코디는 반드시 style_keywords의 방향을 따라야 한다.
- final_style이 특정 스타일이면 그 스타일을 최우선 기준으로 삼는다.
- "미니멀"이라는 단어와 미니멀한 추천은 final_style이 미니멀일 때만 사용한다.
- final_style이 자동 추천 또는 여러 스타일 후보라면 한 가지 미니멀 스타일로 고정하지 말고 2~3가지 선택지를 제안한다.
- final_style이 자동 추천이면 오늘 날씨와 상황에 맞는 스타일 후보를 2~3개 제안한다.
- 사용자가 스트릿을 선택하면 오버핏, 후디, 카고팬츠, 와이드 팬츠, 볼캡, 스니커즈 같은 요소를 반영한다.
- 사용자가 포멀을 선택하면 캐주얼이나 스트릿보다 셔츠, 슬랙스, 재킷, 로퍼처럼 단정한 옷차림을 우선한다.
- 스타일 추천은 날씨 대응과 스타일 다양성을 함께 반영한다.
"""

# ──────────────────────────────────────────────────────────────────────────────
# WEB_SEARCH_LAYER: DuckDuckGo 검색 결과를 어떻게 참고할지 알려줍니다.
# {web_search_result}는 나중에 .format()으로 실제 검색 결과 문자열로 채워집니다.
# ──────────────────────────────────────────────────────────────────────────────
WEB_SEARCH_LAYER = """
DuckDuckGo 검색 참고 정보:
{web_search_result}

웹 검색 사용 규칙:
- 웹 검색 결과는 참고용으로만 사용한다.
- 검색 결과와 사용자 입력이 다르면 사용자 입력과 최종 보정 조건을 우선한다.
- 검색 결과 문장을 그대로 길게 복사하지 말고 옷차림 추천에 필요한 부분만 반영한다.
- 검색 결과가 없거나 오류가 있으면 기존 사용자 조건만으로 답변한다.
- 링크 목록을 답변에 그대로 나열하지 않는다.
"""

# ──────────────────────────────────────────────────────────────────────────────
# RULE_LAYER: 답변 전체의 말투와 추천 기준을 정합니다.
# 초보자도 바로 입을 수 있게 현실적인 옷차림을 제안하도록 지시합니다.
# ──────────────────────────────────────────────────────────────────────────────
RULE_LAYER = """
답변 규칙:
- 첫 줄부터 결론을 먼저 말해.
- 최종 날씨, 최종 기온, 최종 바람을 반드시 반영해.
- 사용자의 상황, 최종 스타일, 체형, 선호 색상을 가능한 한 반영해.
- 비싼 브랜드명보다 옷 종류와 소재 중심으로 추천해.
- 초보자도 바로 입을 수 있게 현실적으로 말해.
- 피하면 좋은 조합은 날씨 대응 관점에서 이유가 보이게 작성해.
- 과장된 표현보다 간결하고 확실한 문장으로 작성해.
"""

# ──────────────────────────────────────────────────────────────────────────────
# RECOMMENDATION_LAYER: 코디 추천에서 고려해야 할 구체적인 기준입니다.
# 기온, 비, 눈, 바람, 상황에 따라 소재와 아이템을 조절하게 합니다.
# ──────────────────────────────────────────────────────────────────────────────
RECOMMENDATION_LAYER = """
추천 기준:
- style_keywords를 코디의 무드, 아이템, 실루엣, 소품 선택의 가장 중요한 기준으로 사용해.
- 기온에 따라 보온, 통기성, 레이어드 정도를 조절해.
- 비나 눈이 있으면 방수성, 미끄럼 방지, 젖어도 티가 덜 나는 색을 고려해.
- 바람이 강하면 펄럭이는 옷과 고정 안 되는 소품을 피하게 해.
- 상황에 맞는 활동성과 단정함의 균형을 잡되, 스타일 개성을 희석하지 마.
"""

# ──────────────────────────────────────────────────────────────────────────────
# OUTPUT_FORMAT_LAYER: AI 답변의 출력 형식을 고정합니다.
# 형식을 고정하면 매번 답변 구조가 비슷해서 화면에 보여주기 쉽습니다.
# final_style이 고정 스타일인지 자동 추천인지에 따라 형식이 달라집니다.
# ──────────────────────────────────────────────────────────────────────────────
OUTPUT_FORMAT_LAYER = """
final_style이 특정 스타일일 때는 아래 형식 그대로 답변해.

오늘의 결론:
[최종 날씨와 선택 스타일을 반영한 결론]

오늘의 무드:
[날씨 + 스타일 이름]

추천 코디:
- 상의:
- 하의:
- 아우터:
- 신발:
- 소품:

날씨 대응 포인트:
- 기온:
- 비/눈/습도:
- 바람:
- 활동성:

피하면 좋은 조합:
-
-

한 줄 요약:
[오늘 옷차림 요약]

스타일 키워드:
#키워드 #키워드 #키워드

final_style이 자동 추천 또는 여러 스타일 후보일 때는 아래 형식 그대로 답변해.

오늘의 결론:
[오늘 날씨에는 한 가지 스타일로 고정하기보다 2~3가지 방향 중 선택하면 좋다는 결론]

추천 스타일 옵션:
1. [스타일명]
- 어울리는 이유:
- 추천 코디:

2. [스타일명]
- 어울리는 이유:
- 추천 코디:

3. [스타일명]
- 어울리는 이유:
- 추천 코디:

날씨 대응 공통 포인트:
- 기온:
- 비/눈/습도:
- 바람:
- 활동성:

피하면 좋은 조합:
-
-

한 줄 요약:
[오늘 옷차림 요약]

스타일 키워드:
#키워드 #키워드 #키워드
"""


def build_weather_fit_prompt(
    user_input: str,
    conversation_history: str,
    context_summary: str,
    weather_keywords: str,
    style_keywords: str,
    warning_keywords: str,
    web_search_result: str = "DuckDuckGo 검색을 사용하지 않았습니다.",
) -> str:
    """프롬프트 layer와 tool 결과를 합쳐 Ollama에 전달할 최종 프롬프트를 만듭니다."""
    # ──────────────────────────────────────────────────────────────────────────
    # app.py와 tools.py에서 만든 값을 받아 최종 프롬프트 한 덩어리로 합칩니다.
    # LLM은 이 긴 문자열 하나만 보고 코디 추천 답변을 생성합니다.
    #
    # context_layer는 매번 사용자별로 달라지는 값들(날씨, 스타일, 사용자 질문 등)을
    # 포함하므로 함수 안에서 f-string으로 동적으로 만듭니다.
    # ──────────────────────────────────────────────────────────────────────────
    context_layer = f"""
CONTEXT_LAYER
최종 보정 조건:
{context_summary}

가장 중요한 스타일 기준:
{style_keywords}

날씨/기온/바람 참고 키워드:
{weather_keywords}

피해야 할 옷차림 힌트:
{warning_keywords}

최근 대화 기록:
{conversation_history or "아직 이전 대화가 없습니다."}

이번 사용자 질문:
{user_input}
"""

    # ──────────────────────────────────────────────────────────────────────────
    # [layers 리스트와 프롬프트 조립 방식]
    #
    # layers 리스트는 LLM에게 정보를 전달하는 순서를 정합니다.
    #   역할 정의 → 안전 규칙 → 우선순위 규칙 → 현재 상황 → 추천 기준 → 출력 형식
    #
    # 각 layer 이름(예: "SYSTEM_LAYER")을 문자열로 넣는 이유:
    #   LLM이 프롬프트를 읽을 때 섹션 구분자처럼 인식해 더 명확하게 이해합니다.
    #
    # WEB_SEARCH_LAYER.format(web_search_result=web_search_result):
    #   WEB_SEARCH_LAYER 문자열 안의 {web_search_result} 자리에
    #   실제 검색 결과 문자열을 삽입합니다.
    #   이것이 Python f-string 대신 .format()을 쓰는 이유입니다.
    #   (WEB_SEARCH_LAYER는 모듈 최상단에서 미리 정의된 상수라 f-string을 쓸 수 없음)
    #
    # .strip(): 문자열 앞뒤의 불필요한 공백과 줄바꿈을 제거합니다.
    # ──────────────────────────────────────────────────────────────────────────
    layers = [
        "SYSTEM_LAYER",
        SYSTEM_LAYER.strip(),
        "SAFETY_LAYER",
        SAFETY_LAYER.strip(),
        "USER_INPUT_PRIORITY_LAYER",
        USER_INPUT_PRIORITY_LAYER.strip(),
        "STYLE_PRIORITY_LAYER",
        STYLE_PRIORITY_LAYER.strip(),
        "WEB_SEARCH_LAYER",
        WEB_SEARCH_LAYER.format(web_search_result=web_search_result).strip(),
        "RULE_LAYER",
        RULE_LAYER.strip(),
        context_layer.strip(),
        "RECOMMENDATION_LAYER",
        RECOMMENDATION_LAYER.strip(),
        "OUTPUT_FORMAT_LAYER",
        OUTPUT_FORMAT_LAYER.strip(),
    ]

    # "\n\n".join(layers) → 각 layer 사이에 빈 줄 하나(두 번 줄바꿈)를 넣어 구분합니다.
    # LLM이 섹션 경계를 더 쉽게 인식하도록 빈 줄로 시각적 구분을 줍니다.
    return "\n\n".join(layers)
